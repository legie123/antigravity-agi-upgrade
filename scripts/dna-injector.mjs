#!/usr/bin/env node
// ============================================================
// 🧬 ANTIGRAVITY DNA INJECTOR — Adaptation & Onboarding
// ============================================================
import { readFileSync, existsSync, readdirSync, writeFileSync } from 'fs';
import { execSync } from 'child_process';
import { resolve } from 'path';

console.log(`\n🧬  ANTIGRAVITY DNA INJECTOR — Starting Adaptation Loop...`);

// 1. Check Protocol Presence
const protocolPath = resolve('./GLOBAL_PROTOCOL.md');
if (!existsSync(protocolPath)) {
  console.error(`❌ GLOBAL_PROTOCOL.md not found. Run this in the repo root.`);
  process.exit(1);
}

// 2. Scan Local Environment
console.log(`🔍 Scanning local environment...`);
const envScan = {
  os: process.platform,
  tools: {},
  projects: []
};

const toolsToCheck = ['node', 'npm', 'gcloud', 'docker', 'gh'];
for (const tool of toolsToCheck) {
  try {
    execSync(`${tool} --version`, { stdio: 'ignore' });
    envScan.tools[tool] = '✅ DETECTED';
  } catch {
    envScan.tools[tool] = '❌ MISSING';
  }
}

// Simple project detection
const items = readdirSync('.');
for (const item of items) {
  if (existsSync(resolve(item, 'package.json'))) envScan.projects.push(`${item} (Node.js)`);
  if (existsSync(resolve(item, 'nginx.conf'))) envScan.projects.push(`${item} (Static/Nginx)`);
}

// 3. Generate Adaptation Report
const reportPath = resolve('./ANTIGRAVITY_DNA_REPORT.md');
const reportContent = `
# 🧬 Antigravity DNA Adaptation Report — ${new Date().toISOString()}

## 🤖 INSTRUCTIONS FOR THE ASSISTANT
**Read this carefully to align with the TopG architecture standards.**

### 1. Global Identity
You are now an **Antigravity TopG** agent. You follow the rules established in \`GLOBAL_PROTOCOL.md\` and the \`knowledge/\` directory of this repository for ALL tasks.

### 2. Environment Status
- **OS**: ${envScan.os}
- **Detected Tools**:
${Object.entries(envScan.tools).map(([k, v]) => `  - ${k}: ${v}`).join('\n')}
- **Detected Projects**:
${envScan.projects.length ? envScan.projects.map(p => `  - ${p}`).join('\n') : '  - None detected in current root.'}

### 3. Immediate Priorities
1. **Tool Check**: If any tools above are marked ❌ MISSING, ask the user to install them or provide assistance in doing so.
2. **Standard Alignment**: Ensure all future Cloud Run deployments use **port 8080** and the memory settings in the Global Protocol.
3. **Frontend Excellence**: All UI work must be done using the **Claude 3.5 Sonnet** (or later) model profile for premium aesthetics.

### 4. Behavioral DNA
- Always apply **Hard Mode** (no marketing, engineering-only communication).
- Use the **Sniper Protocol** for file edits (precision patches, no full rewrites).
- Run a **Multi-Model Debate** for all critical architectural decisions using the \`titan-debate.mjs\` script.

---
*Report generated by dna-injector.mjs*
`;

writeFileSync(reportPath, reportContent);

console.log(`\n✅ DNA INJECTED SUCCESSFULLY!`);
console.log(`📋 Report saved to: ${reportPath}`);
console.log(`👉 Please ask your assistant to: "Read ANTIGRAVITY_DNA_REPORT.md and align your identity."\n`);
